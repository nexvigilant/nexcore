//! NexCore Watch App — Pure Rust NativeActivity entry point with Slint UI.
//!
//! ## Architecture
//!
//! ```text
//! android_main()
//!   └── WatchSystem::boot(SystemConfig::default())
//!         ├── PVOS::boot()       ← 15-layer OS
//!         ├── SyncManager::new() ← background sync
//!         └── alert queue        ← bounded P0-P5 queue
//!                ↓
//!         Slint UI loop
//!           ├── GuardianScreen   ← ς state display
//!           ├── SignalScreen     ← κ metrics display
//!           └── AlertsScreen     ← ∂ priority display
//! ```
//!
//! ## Primitive Grounding
//! - × (Product): app = system × UI × platform
//! - σ (Sequence): boot → init_slint → event_loop
//! - → (Causality): system.tick() → UI property updates → render
//!
//! ## Tier: T3 (platform-specific, 100% Rust)

