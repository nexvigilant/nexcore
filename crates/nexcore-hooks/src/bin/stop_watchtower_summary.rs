//! stop_watchtower_summary - Save session summary to Brain on session end
//!
//! Fires on Stop event to create a Brain artifact with session statistics
//! and extracted primitives for cross-session learning.
//!
//! Protocol: Exit 0 (informational)

use serde::Deserialize;
use std::fs;
use std::io::{self, Write};
use std::path::PathBuf;
use std::process::Command;

#[derive(Deserialize)]
struct HookInput {
    session_id: Option<String>,
}

fn main() {
    let stdin = io::stdin();
    let input: HookInput = match serde_json::from_reader(stdin.lock()) {
        Ok(i) => i,
        Err(_) => std::process::exit(0),
    };

    let session_id = input
        .session_id
        .unwrap_or_else(|| "unknown".to_string())
        .chars()
        .take(8)
        .collect::<String>();

    // Get home directory
    let home = match dirs::home_dir() {
        Some(h) => h,
        None => std::process::exit(0),
    };

    // Read commands.log and filter to this session
    let commands_log = home.join(".claude/logs/commands.log");
    if !commands_log.exists() {
        std::process::exit(0);
    }

    let content = fs::read_to_string(&commands_log).unwrap_or_default();

    // Count operations for this session
    let session_filter = format!("[{}]", session_id);
    let mut bash_count = 0;
    let mut read_count = 0;
    let mut write_count = 0;
    let mut edit_count = 0;
    let mut task_count = 0;
    let mut mcp_count = 0;

    for line in content.lines() {
        if !line.contains(&session_filter) {
            continue;
        }
        if line.contains("Bash:") {
            bash_count += 1;
        }
        if line.contains("Read:") {
            read_count += 1;
        }
        if line.contains("Write:") {
            write_count += 1;
        }
        if line.contains("Edit:") {
            edit_count += 1;
        }
        if line.contains("Task") {
            task_count += 1;
        }
        if line.contains("mcp__") {
            mcp_count += 1;
        }
    }

    let total = bash_count + read_count + write_count + edit_count + task_count + mcp_count;

    if total < 3 {
        // Session too short to summarize
        std::process::exit(0);
    }

    // Generate summary
    let timestamp = chrono::Utc::now().format("%Y-%m-%d %H:%M:%S UTC");
    let summary = format!(
        r#"# Session Summary: {}

Generated: {}

## Statistics
| Metric | Count |
|--------|-------|
| Total Operations | {} |
| Bash Commands | {} |
| File Reads | {} |
| File Writes | {} |
| File Edits | {} |
| Task Spawns | {} |
| MCP Calls | {} |

## Detected Patterns
- Read/Write Ratio: {:.2}
- Session Type: {}

## Notes
Auto-generated by watchtower on session end.
"#,
        session_id,
        timestamp,
        total,
        bash_count,
        read_count,
        write_count,
        edit_count,
        task_count,
        mcp_count,
        read_count as f64 / (write_count + edit_count + 1) as f64,
        if read_count > write_count + edit_count {
            "Exploration"
        } else {
            "Construction"
        }
    );

    // Save to watchtower brain session
    let artifact_dir = home.join(".claude/brain/sessions/watchtower");
    if !artifact_dir.exists() {
        if let Err(e) = fs::create_dir_all(&artifact_dir) {
            eprintln!("Failed to create artifact dir: {}", e);
            std::process::exit(0);
        }
    }

    let artifact_path = artifact_dir.join(format!("session-{}.md", session_id));
    if let Err(e) = fs::write(&artifact_path, &summary) {
        eprintln!("Failed to write artifact: {}", e);
    } else {
        eprintln!("📊 Session summary saved: {}", artifact_path.display());
    }

    std::process::exit(0);
}
